public class RightAngledTriangle {

    public static void main(String[] args) {
        System.out.println(" *************** Right Angled Tringle Pattern *********** ");
        for (int i = 0;i < 4;i++){
                for (int j = 0;j<=i;j++){
                    System.out.print("* ");
                }
                System.out.println();
            }
    }
}
